export function ShareButtons({ url, title }: { url: string; title: string }) {
  const [copied, setCopied] = useState(false);

  const encodedUrl = encodeURIComponent(url);
  const encodedTitle = encodeURIComponent(title);

  const copyLink = async () => {
    try {
      await navigator.clipboard.writeText(url);
      setCopied(true);
      setTimeout(() => setCopied(false), 2000);
    } catch {
      // clipboard unavailable — no-op
    }
  };

  const linkClass =
    "inline-flex items-center justify-center w-9 h-9 rounded-full border border-border text-muted-foreground hover:border-primary/50 hover:text-primary transition-colors";

  return (
    <div className="flex items-center gap-3 flex-wrap">
      <span className="text-muted-foreground" style={{ fontSize: "0.8rem" }}>Share</span>
      <a
        href={`https://twitter.com/intent/tweet?url=${encodedUrl}&text=${encodedTitle}`}
        target="_blank"
        rel="noopener noreferrer"
        aria-label="Share on X"
        className={linkClass}
      >
        <XIcon className="w-4 h-4" />
      </a>
      <a
        href={`https://www.linkedin.com/sharing/share-offsite/?url=${encodedUrl}`}
        target="_blank"
        rel="noopener noreferrer"
        aria-label="Share on LinkedIn"
        className={linkClass}
      >
        <LinkedInIcon className="w-4 h-4" />
      </a>
      <button onClick={copyLink} aria-label="Copy link" className={linkClass}>
        {copied ? <Check className="w-4 h-4" /> : <Link2 className="w-4 h-4" />}
      </button>
      <a href="/feed.xml" aria-label="Subscribe via RSS" className={linkClass}>
        <Rss className="w-4 h-4" />
      </a>
    </div>
  );
}
